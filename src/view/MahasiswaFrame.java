    private void tbnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbnTambahActionPerformed

        int status = 0;
        try {
            DefaultTableModel model = (DefaultTableModel) tblMahasiswa.getModel();
            status = con.insert(new Mahasiswa(tfNrp.getText(), tfNama.getText(),tfJurusan.getText(), tfEmail.getText()));

            refreshTable();
        } catch (SQLException ex) {
            Logger.getLogger(MahasiswaFrame.class.getName()).log(Level.SEVERE, null, ex);

        }
        if (status == 1) {
            JOptionPane.showMessageDialog(this, "Mahasiswa Berhasil Ditambahkan");
        } else {
            JOptionPane.showMessageDialog(this, "Mahasiswa Gagal Di Tambahkan");
        }
    }//GEN-LAST:event_tbnTambahActionPerformed

