    public void populateDataToTable() throws SQLException {
        model = (DefaultTableModel) tblMahasiswa.getModel();
        List<Mahasiswa> mhs = con.loadMahasiswas();

        for (Mahasiswa mh : mhs) {
            String[] row = new String[4];
            row[0] = mh.getNrp();
            row[1] = mh.getNama();
            row[2] = mh.getJurusan();
            row[3] = mh.getEmail();
            model.addRow(row);
        }

    }

    public void refreshTable() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) tblMahasiswa.getModel();
        model.setRowCount(0);
        populateDataToTable();
    }

